def get_lang() -> str:
    """Zwraca aktualny język: 'pl' lub 'en'."""
    return st.session_state.get("_lang", "pl")


def t(key: str, **kwargs) -> str:
    """
    Zwraca przetłumaczony string dla danego klucza.
    Jeśli klucz nie istnieje, zwraca sam klucz (fallback).
    Obsługuje format kwargs: t("gs_weight_ok", w=99.5)
    """
    lang = get_lang()
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key  # fallback — zwróć klucz
    text = entry.get(lang) or entry.get("pl") or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def t_list(key: str) -> list:
    """Zwraca przetłumaczoną listę (dla kluczy z wartościami list)."""
    lang = get_lang()
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return []
    val = entry.get(lang) or entry.get("pl") or []
    return val if isinstance(val, list) else [val]
